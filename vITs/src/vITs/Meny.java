    private String hamtaUtgifter() {
        String utgifter = "";
        for (int y = 0; y < tblUtgifter.getRowCount(); y++) {
            for (int x = 0; x < tblUtgifter.getColumnCount(); x++) {
                String utgift = tblUtgifter.getValueAt(y, x).toString();
                utgifter += " " + utgift;
            }
            utgifter += "\n";
        }
        return utgifter;
    }

